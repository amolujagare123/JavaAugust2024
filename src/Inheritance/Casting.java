package Inheritance;

public class Casting {

    public static void main(String[] args) {

        int a = 10;
        int b = 3;
        double c = (double) a / b;

        // if you want to store one type of value into
        // another type then use that type into round
        // brackets in front of the value to be stored
        // this is called as Casting

        System.out.println("c="+c);
    }
}
